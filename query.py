_NUMERIC_KEYWORDS = {
    "revenue", "profit", "loss", "income", "expense", "expenses", "cost", "costs",
    "ebitda", "ebit", "margin", "tax", "asset", "assets", "liability", "liabilities",
    "equity", "cash", "debt", "balance", "sheet", "total", "subtotal", "amount",
    "amounts", "value", "values", "price", "prices", "ratio", "ratios", "percent",
    "percentage", "growth", "yoy", "quarter", "fy", "year", "annual",
    "how", "much", "many", "number", "count",
}


def is_numeric_question(question):
    q = question.lower()
    tokens = set(re.findall(r"[a-z]+", q))
    if tokens & _NUMERIC_KEYWORDS:
        return True
    if re.search(r"\$|₹|€|£|\bfy\d{2,4}\b|\b(19|20)\d{2}\b|\d[\d,]*\.?\d*", q):
        return True
    return False


_FIGURE_KEYWORDS = {
    "figure", "figures", "diagram", "diagrams", "chart", "charts", "image",
    "images", "screenshot", "screenshots", "depict", "depicts", "show", "shows",
    "architecture", "flow", "flowchart", "schema", "schematic",
}


def is_figure_question(question):
    return bool(set(re.findall(r"[a-z]+", question.lower())) & _FIGURE_KEYWORDS)


# --- financial-statement intent --------------------------------------------

# Map query phrases -> the statement-title keyword to look for in `section` meta.
_STATEMENT_INTENTS = {
    "profit and loss": ["profit and loss", "profit & loss", "net profit",
                        "profit for the year", "income statement", "p&l", "revenue",
                        "total income", "earnings"],
    "balance sheet": ["balance sheet", "total assets", "total equity",
                      "liabilities", "net worth"],
    "cash flow": ["cash flow", "operating activities", "investing activities",
                 "financing activities"],
    "changes in equity": ["changes in equity"],
}

_VARIANTS = ["standalone", "consolidated"]


def detect_statement_targets(question):
    """Return list of (variant_or_None, statement_keyword) the question implies.
    e.g. 'standalone vs consolidated net profit' -> [('standalone','profit and loss'),
    ('consolidated','profit and loss')]."""
    q = question.lower()
    statements = [stmt for stmt, kws in _STATEMENT_INTENTS.items()
                  if any(kw in q for kw in kws)]
    if not statements:
        return []
    variants = [v for v in _VARIANTS if v in q]
    targets = []
    for stmt in statements:
        if variants:
            for v in variants:
                targets.append((v, stmt))
        else:
            targets.append((None, stmt))
    return targets


_BOARD_KEYWORDS = {
    "director", "directors", "board", "chairman", "chairperson", "chairwoman",
    "independent", "executive", "non-executive", "auditor", "auditors",
    "committee", "secretary", "cfo", "officer", "management", "kmp",
}

_BOARD_PROBE = ("Board of Directors Non-Executive Chairman Independent Director "
                "Executive Director Company Secretary Chief Financial Officer")


def is_board_question(question):
    return bool(set(re.findall(r"[a-z-]+", question.lower())) & _BOARD_KEYWORDS)


def _doc_matches_statement(doc, variant, statement):
    """Match a chunk to a (variant, statement) target. Table chunks carry a
    `section` label; text chunks embed the statement title in their content, so
    check both. Table extraction sometimes drops rows (e.g. 'Profit for the
    Year'), so the complete text chunk is often the more reliable source."""
    section = (doc.metadata.get("section") or "").lower()
    # First ~300 chars of content usually holds the statement title line.
    head = doc.page_content[:300].lower()
    hay = section + " " + head
    if statement not in hay:
        return False
    if variant and variant not in hay:
        return False
    return True


# --- context formatting -----------------------------------------------------

_INDIAN_NUM_RE = re.compile(r"\b\d{1,3}(?: \d{2})+\b")


def _normalize_indian_numbers(text):
    """Collapse Indian space-grouped numbers into clean comma-formatted integers
    so the LLM reads unambiguous values (e.g. '9 83' -> '983', '446 64' -> '44,664').
    Only matches sequences of a 1-3 digit lead followed by 2-digit groups, which
    is specific enough to avoid mangling version numbers or years."""
    def repl(m):
        digits = m.group(0).replace(" ", "")
        try:
            return f"{int(digits):,}"
        except ValueError:
            return m.group(0)
    return _INDIAN_NUM_RE.sub(repl, text)


def format_context(docs):
    parts = []
    for i, d in enumerate(docs, 1):
        src = d.metadata.get("source", "?")
        page = d.metadata.get("page", "?")
        kind = d.metadata.get("type", "text")
        # Period / company are also surfaced here so EVERY chunk (including
        # 2nd/3rd sub-chunks of a split page, which lose the in-text header)
        # carries the attribution the LLM needs to answer period-scoped
        # questions correctly.
        company = d.metadata.get("company") or ""
        period = d.metadata.get("period") or ""
        attr_bits = [f"{src} p.{page}"]
        if company:
            attr_bits.append(company)
        if period:
            attr_bits.append(period)
        attr = " · ".join(attr_bits)
        tag = {"table": "TABLE", "figure": "FIGURE",
               "cached_facts": "CACHED-FACTS"}.get(kind, "TEXT")
        body = _normalize_indian_numbers(d.page_content)
        if kind == "figure":
            # Framed so the LLM treats granite's output as authoritative content,
            # not as a meta-description to be discounted.
            body = (
                "(This [FIGURE] chunk is the AUTHORITATIVE TRANSCRIPTION of a figure "
                "from the PDF, produced by a vision model. When the user asks about "
                "this figure, draw your answer directly from the text below; do NOT "
                "say the figure is missing or only described.)\n\n" + body
            )
        parts.append(f"[{i}] ({attr}) [{tag}]\n{body}")
    return "\n\n".join(parts)


def _dedupe(docs):
    seen = set()
    out = []
    for d in docs:
        key = (d.metadata.get("source"), d.metadata.get("page"),
               d.metadata.get("type"), d.page_content[:80])
        if key in seen:
            continue
        seen.add(key)
        out.append(d)
    return out


# Currency disambiguation -- Infosys quarterly press releases publish the
# statement of comprehensive income TWICE: once in INR (page 3) and once in
# USD (page 4). Both pages embed nearly identical labels ("Revenue", "Cost of
# Sales", ...) so they rank equally on similarity, then the model mixes
# crore-scale and million-scale values and produces nonsense. Detect each
# chunk's reporting unit from the table header it carries and stable-sort the
# final list INR-first, so the MAX_CONTEXT_CHUNKS cap demotes USD pages first.
# This keeps the USD content available when it's the only thing that matched,
# but never lets it crowd out the INR version of the same statement.
_USD_MARKERS = ("in us $", "in us$", "us$ millions", "us $ millions",
                "(in usd", "(in us $", "(in us$",
                "statement of comprehensive income\n(in us")
_INR_MARKERS = ("in ₹ crore", "in ` crore", "in rs. crore", "in rs crore",
                "(in ₹", "(in `", "₹ crore", "rs. crore",
                "(in inr")


_USD_PER_SHARE_RE = re.compile(r"\beps\s*\(\$\)", re.IGNORECASE)
_INR_PER_SHARE_RE = re.compile(r"\beps\s*\(\s*(?:₹|`|rs\.?)\s*\)", re.IGNORECASE)
_DOLLAR_VALUE_RE = re.compile(r"\$\s?\d")

# Magnitude tiebreaker: balance-sheet pages often lose their unit caption to
# the text splitter, so explicit markers miss. For Infosys-scale companies:
#   - INR crore pages always print a >= 50,000 line (total assets ~1,45,000
#     cr; total equity+liabilities; nine-month revenues).
#   - USD million pages top out around $17,000M, never reaching 50,000.
# So >= 50,000 -> INR, < 5,000 max -> USD, anything between stays unknown
# (safer than wrong; the chunk lands mid-ranking, not last).
# The regex accepts both Western grouping ("145,452") and Indian grouping
# ("1,45,452") which is how PyMuPDF tends to render Infosys' rupee tables.
_LARGE_NUMBER_RE = re.compile(r"\b\d{1,3}(?:,\d{2,3})+(?:\.\d+)?\b|\b\d{5,}\b")
_INR_MAGNITUDE_THRESHOLD = 50_000
_USD_MAGNITUDE_CEILING = 5_000


def _max_numeric_value(text):
    """Largest plain integer in the chunk (commas stripped). Returns 0 on no match."""
    best = 0
    for m in _LARGE_NUMBER_RE.finditer(text):
        try:
            v = int(m.group(0).replace(",", "").split(".")[0])
            if v > best:
                best = v
        except ValueError:
            continue
    return best


def _doc_currency(doc):
    """Classify a chunk as 'inr' | 'usd' | None.

    Tries three signals in order so we still classify sub-chunks where the
    "(In ₹ crore...)" caption was split away by the text splitter:
      1. Explicit unit caption in the chunk head ("(In ₹ crore...)" / "(in US $ millions)").
      2. "EPS (₹)" vs "EPS ($)" — appears once per statement at the bottom and
         tends to survive splitting alongside the totals rows.
      3. $-prefixed values anywhere ('$ 4,663') as a USD tiebreaker.
    Conservative — anything ambiguous stays None and lands in the middle of
    the ordering, never the back."""
    text = doc.page_content
    head = text[:600].lower()
    if any(m in head for m in _USD_MARKERS):
        return "usd"
    if any(m in head for m in _INR_MARKERS):
        return "inr"
    if _USD_PER_SHARE_RE.search(text):
        return "usd"
    if _INR_PER_SHARE_RE.search(text):
        return "inr"
    if _DOLLAR_VALUE_RE.search(text):
        return "usd"
    # Magnitude fallback for caption-less balance-sheet sub-chunks.
    biggest = _max_numeric_value(text)
    if biggest >= _INR_MAGNITUDE_THRESHOLD:
        return "inr"
    if 0 < biggest < _USD_MAGNITUDE_CEILING:
        return "usd"
    return None


def _prefer_inr(docs):
    """Stable reorder: INR first, unknown next, USD last."""
    inr, unk, usd = [], [], []
    for d in docs:
        c = _doc_currency(d)
        (inr if c == "inr" else usd if c == "usd" else unk).append(d)
    return inr + unk + usd


def _drop_redundant_usd(docs):
    """For any source PDF where an INR chunk is present, drop the USD chunks
    of the SAME source. Infosys press releases publish the same statement in
    both units; once we have the INR version, the USD version is a duplicate
    in a different scale that only invites the LLM to misread the unit (e.g.
    quoting US$ 1,640M as ₹1,640 cr). Unknown-currency chunks are always kept.

    Only applies per-source so we never drop the only chunk we have for some
    period — if a period's only retrieval hit happens to be USD, it stays."""
    inr_sources = {d.metadata.get("source") for d in docs
                   if _doc_currency(d) == "inr" and d.metadata.get("source")}
    if not inr_sources:
        return docs
    out = []
    for d in docs:
        if (_doc_currency(d) == "usd"
                and d.metadata.get("source") in inr_sources):
            continue
        out.append(d)
    return out


