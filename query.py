def _period_label_to_filter_kwargs(label):
    """Convert a period label ('Q3FY24' or 'FY24') into the kwargs that should
    be passed to the filtered retrievers.

    Quarterlies map to an exact `period_filter` (chunks tag matches 1:1).
    Annuals map to a numeric `fy_filter` instead, because the corpus has
    quarterly chunks tagged Q*FY24 (not FY24) and we want them to match an
    "FY24" mention. This also lets a single "FY24" range bucket pick up the
    full-year column that sometimes appears inside a quarterly report."""
    parsed = _parse_period_label(label)
    if not parsed:
        return {"period_filter": label}
    kind, fy, _ = parsed
    if kind == "annual":
        return {"fy_filter": fy}
    return {"period_filter": label}


def retrieve(question, source_filter=None, multi_sources=None,
             company_filter=None, period_filter=None, periods=None,
             top_k=None):
    """Hybrid (BM25 + vector) retrieval with type / multi-source biasing.

    multi_sources: iterable of filenames. When set, retrieval is RESTRICTED to
        those sources (no corpus-wide fallback) so unrelated authors can't
        leak into the comparison context.
    company_filter / period_filter: hard filters applied to BOTH BM25 and vector
        when exactly one company / period is detected. These are passed through
        to every hybrid call below.
    periods: iterable of period labels for range/trend questions ("Q1FY25
        through Q3FY26"). When set, retrieval fans out one pass per period and
        merges, so every period in the range gets seats in the context.
    top_k: optional override for config.TOP_K — modes like analyze/compare use
        this to pull a larger base context.
    """
    top_k = top_k or config.TOP_K
    numeric = is_numeric_question(question)
    figure = is_figure_question(question)

    # Multi-period fan-out comes FIRST: ranges/trends span periods, so we want
    # an equal slice per period rather than letting hybrid pick the most
    # textually-similar one (which tends to mean a single period dominates).
    if periods and len(periods) >= 2 and not period_filter:
        docs = []
        anchor_docs = []
        k_per = max(3, top_k // len(periods) + 2)
        # Per-period anchor probe. We pick the BEST anchor chunk per period
        # and promote it to the front of the context so MAX_CONTEXT_CHUNKS
        # can't drop the actual figures page when fan-out is heavy.
        # Probe and the "this chunk is the right table" gate depend on which
        # financial statement the user is asking about. Default = income
        # statement (the most common case); switches when detect_statement_targets
        # names balance sheet / cash flow.
        targets = detect_statement_targets(question)
        target_stmts = {stmt for _, stmt in targets}
        if "balance sheet" in target_stmts:
            anchor_probe = ("Total assets Total equity Total liabilities "
                            "Cash and cash equivalents Trade receivables "
                            "Property plant and equipment in rupees crore")
            anchor_must_have = ("Total assets", "Total equity")
        elif "cash flow" in target_stmts:
            anchor_probe = ("Cash flow from operating activities investing "
                            "financing activities net increase decrease in cash")
            anchor_must_have = ("operating activities", "investing activities")
        else:
            anchor_probe = (
                "Revenues Cost of Sales Gross profit Operating profit "
                "Net profit Basic EPS in rupees crore"
            )
            anchor_must_have = ("Revenues", "Operating profit")
        for p in periods:
            p_kwargs = _period_label_to_filter_kwargs(p)
            docs += _hybrid_retrieve(question, k=k_per,
                                     source_filter=source_filter,
                                     company_filter=company_filter,
                                     **p_kwargs)
            if numeric:
                docs += _hybrid_retrieve(question, k=max(2, k_per // 2),
                                         source_filter=source_filter,
                                         company_filter=company_filter,
                                         type_filter="table", **p_kwargs)
                # Per-period anchor: prefer INR, pick the FIRST chunk that
                # contains the target statement's signature rows. One
                # guaranteed slot per period.
                # type_filter="table" is good for the income-statement default
                # (the IS is reliably extracted as a table), but balance-sheet
                # and cash-flow sections frequently bleed into text chunks
                # because pdfplumber misses the table boundary. Drop the
                # type filter when a statement target is set so a text chunk
                # carrying "Total assets ... Total equity" can win.
                anchor_type_filter = None if targets else "table"
                cand = _hybrid_retrieve(anchor_probe, k=8,
                                        source_filter=source_filter,
                                        company_filter=company_filter,
                                        type_filter=anchor_type_filter,
                                        **p_kwargs)
                cand = _prefer_inr(cand)
                picked = None
                for d in cand:
                    if all(kw in d.page_content for kw in anchor_must_have):
                        picked = d
                        break
                if picked is None and cand:
                    picked = cand[0]
                if picked is not None:
                    anchor_docs.append(picked)
                # When a statement target is named, ALSO pull every chunk from
                # the anchor's page — table extraction often splits the
                # balance sheet across multiple chunks (one per section) and
                # the labelled-totals row can land in a different chunk than
                # the rest. Same logic the single-period path already uses.
                if targets and picked is not None:
                    src = picked.metadata.get("source")
                    pg = picked.metadata.get("page")
                    if src and pg:
                        anchor_docs += [d for d in _all_docs()
                                        if d.metadata.get("source") == src
                                        and d.metadata.get("page") == pg]
        # Anchors first so they survive the MAX_CONTEXT_CHUNKS cap, then the
        # rest of the fan-out hits in INR-preferred order. Drop redundant USD
        # duplicates of any source that also has an INR chunk — keeps the LLM
        # from misreading US$ millions as ₹ crore.
        ordered = _dedupe(anchor_docs) + _prefer_inr(_dedupe(docs))
        ordered = _drop_redundant_usd(_dedupe(ordered))
        return ordered[:config.MAX_CONTEXT_CHUNKS], numeric

    if multi_sources and not source_filter:
        # Multi-author comparison: per-source pull only, equal seats each.
        # We retrieve TWO query variants per source:
        #   1. The literal comparison query (catches direct hits)
        #   2. A synthetic "describe this project" probe per author, scoped to
        #      that source — guarantees foundational intro/abstract chunks land
        #      in context so the LLM can actually describe each project.
        docs = []
        k_per = max(3, config.TOP_K // len(multi_sources) + 2)
        for src in multi_sources:
            author = _AUTHOR_INDEX_BY_FILENAME.get(src, "")
            probe = (f"what is {author}'s project about, its scope, technologies, "
                     f"and key outcomes") if author else \
                    f"what is this project about, its scope, technologies, outcomes"
            docs += _hybrid_retrieve(question, k=k_per, source_filter=src)
            docs += _hybrid_retrieve(probe, k=k_per, source_filter=src)
            if numeric:
                docs += _hybrid_retrieve(question, k=max(2, k_per // 2),
                                         source_filter=src, type_filter="table")
            if figure:
                docs += _hybrid_retrieve(question, k=max(2, k_per // 2),
                                         source_filter=src, type_filter="figure")
        return _dedupe(docs), numeric

    # From here down: standard single-target retrieval. company_filter and
    # period_filter (when set) are forwarded to every hybrid call so the
    # statement / board / type bonuses and the base all stay scoped to the
    # named company/period. Annual single-period filters are converted to a
    # numeric fy filter so they match the quarterly chunks of that FY.
    flt = {"source_filter": source_filter,
           "company_filter": company_filter}
    if period_filter:
        flt.update(_period_label_to_filter_kwargs(period_filter))

    # Single-source or unfiltered path. Ordered by priority — most important
    # bonus first, because the total is capped at MAX_CONTEXT_CHUNKS.

    # 1. Statement-targeted (highest priority): for each (variant, statement)
    #    the question implies, find the statement's page(s) and pull EVERY chunk
    #    from them. Table extraction can drop rows (e.g. 'Profit for the Year'),
    #    and the title vs the figure often land in different sub-chunks, so the
    #    whole page is needed to guarantee complete, correctly-labelled figures.
    statement_bonus = []
    for variant, statement in detect_statement_targets(question):
        probe = f"{variant or ''} {statement}".strip()
        cand = _hybrid_retrieve(probe, k=top_k * 2, **flt)
        target_pages = {(d.metadata.get("source"), d.metadata.get("page"))
                        for d in cand if _doc_matches_statement(d, variant, statement)}
        for src, pg in target_pages:
            statement_bonus += [d for d in _all_docs()
                                if d.metadata.get("source") == src
                                and d.metadata.get("page") == pg]

    # 1b. Board / people questions: the actual board-listing page (often the
    #     "Company Information" page) loses to the Corporate Governance Report,
    #     which is dense with "director" mentions. Target the listing page.
    board_bonus = []
    if is_board_question(question):
        cand = _hybrid_retrieve(_BOARD_PROBE, k=top_k, **flt)
        # Prefer pages that actually LIST roles (high count of role labels).
        def role_score(d):
            t = d.page_content.lower()
            return sum(t.count(r) for r in ("director", "chairman", "secretary",
                                            "officer", "auditor"))
        top_pages = sorted({(d.metadata.get("source"), d.metadata.get("page"))
                            for d in cand if role_score(d) >= 3})
        for src, pg in top_pages[:2]:
            board_bonus += [d for d in _all_docs()
                            if d.metadata.get("source") == src
                            and d.metadata.get("page") == pg]

    # 2. Type bias.
    type_bonus = []
    if numeric:
        type_bonus += _hybrid_retrieve(question, k=max(1, top_k // 2),
                                       type_filter="table", **flt)
    if figure:
        type_bonus += _hybrid_retrieve(question, k=max(1, top_k // 2),
                                       type_filter="figure", **flt)

    # 3. General hybrid base.
    base = _hybrid_retrieve(question, k=top_k, **flt)

    docs = _prefer_inr(_dedupe(statement_bonus + board_bonus + type_bonus + base))
    docs = _drop_redundant_usd(docs)[:config.MAX_CONTEXT_CHUNKS]
    return docs, numeric


def _history_to_messages(history):
    """Convert [{'role': 'user'|'assistant', 'content': str}, ...] to LC messages.
    Trims to the last HISTORY_TURNS entries."""
    if not history:
        return []
    window = history[-config.HISTORY_TURNS:]
    msgs = []
    for turn in window:
        role = turn.get("role")
        content = turn.get("content", "")
        if role == "user":
            msgs.append(HumanMessage(content=content))
        elif role == "assistant":
            msgs.append(AIMessage(content=content))
    return msgs


_PRONOUN_RE = re.compile(
    r"\b(it|its|he|him|his|she|her|hers|they|them|their|theirs|that|this|those|these|"
    r"the (?:one|first|second|third|fourth|last|previous|same|other|former|latter))\b",
    re.IGNORECASE,
)
_ENTITY_HINT_RE = re.compile(
    r"\bfy\s*\d{2,4}\b|\bq[1-4]\b|\b(?:19|20)\d{2}\b|"
    r"\b(?:infosys|infy|riil|reliance)\b",
    re.IGNORECASE,
)


def _is_self_contained(question):
    """True if the question names its own entities (FY/Q/year/company) AND has
    no back-references (pronouns, "that one", "the previous"). For these we
    skip the rewriter entirely — it can only hurt (cf. the FY24/FY25 query
    that got rewritten with FY26 + 'stock price' noise)."""
    if _PRONOUN_RE.search(question):
        return False
    return bool(_ENTITY_HINT_RE.search(question))


def rewrite_query(question, history, llm=None):
    """Rewrite a follow-up into a standalone search query. Skipped when:
      - there is no history (first turn), or
      - the question already names its own entities and has no pronouns.
    Both cases save a model call AND prevent the rewriter from inventing
    extra entities ('FY26', 'stock price') that pollute slot detection."""
    if not history:
        return question
    if _is_self_contained(question):
        return question
    llm = llm or ChatOllama(model=config.LLM_MODEL, base_url=config.OLLAMA_BASE_URL, temperature=0)
    chain = REWRITE_PROMPT | llm | StrOutputParser()
    rewritten = chain.invoke({
        "history": _history_to_messages(history),
        "question": question,
    }).strip().strip('"').strip("'")
    return rewritten or question


def ask(question, history=None, llm=None, mode=None, upload_ids=None):
    """Run one RAG turn.

    Args:
        question: the user's raw input.
        history: optional list of prior turns [{"role": ..., "content": ...}, ...].
        llm: optional pre-built ChatOllama (used for tests/reuse). NOTE: when
             mode is set, this is ignored in favour of a per-mode LLM so each
             mode can use its own temperature.
        mode: one of MODES keys ("extract", "analyze", "compare"). Defaults to
              "extract" (current behavior). Picks the system prompt and per-mode
              retrieval/LLM tuning.

    Returns dict with answer, sources, filter info, numeric/period/company
    flags, mode, and rewritten_query.
    """
    history = history or []
    # Normalised once at the top so every downstream branch (rewriter skip,
    # cache short-circuit gate, retrieval merge, return payload) sees the
    # same shape.
    upload_ids = list(upload_ids or [])
    has_uploads = bool(upload_ids)
    mode_name = (mode or DEFAULT_MODE).lower()
    mode_cfg = get_mode(mode_name)
    # Per-mode LLM (lets analyze/compare use a different temperature later).
    # If the caller passed an llm AND no explicit mode override, reuse it for
    # backward compat; otherwise build a fresh one with the mode's temperature.
    if llm is not None and mode is None:
        pass  # use the caller's llm
    else:
        llm = ChatOllama(model=config.LLM_MODEL,
                         base_url=config.OLLAMA_BASE_URL,
                         temperature=mode_cfg["temperature"])

    # Mode-specific prompt template. History placeholder + human turn match the
    # original PROMPT layout exactly.
    mode_prompt = ChatPromptTemplate.from_messages([
        ("system", mode_cfg["system_prompt"]),
        MessagesPlaceholder("history"),
        ("human", "{question}"),
    ])

    # When uploads are attached, skip the rewriter entirely. The upload
    # chips already tell us what the user is asking about; the LLM rewriter
    # tends to drag in entities from the LAST chat turn ("describe the
    # architecture diagram" -> rewritten to last turn's "compare riyansh
    # vs prakarsh" because the rewriter saw that in history). The original
    # question is fine -- it'll hit the upload's BM25/vector index directly.
    if has_uploads:
        rewritten = question
    else:
        rewritten = rewrite_query(question, history, llm=llm)
    # Run filter detection over BOTH the original and the rewritten query so
    # rewriter-induced spelling drift doesn't lose a real author match.
    all_sources = detect_all_sources(question, rewritten)
    source_filter = next(iter(all_sources)) if len(all_sources) == 1 else None
    multi_sources = all_sources if len(all_sources) >= 2 else None

    # Company/period intent (financial corpora).
    # PRIMARY: LLM structured extraction over the ORIGINAL question only.
    #          Feeding `rewritten` here previously let the rewriter inject
    #          entities the user never asked about (e.g. FY26 into a FY24/FY25
    #          query) and pollute the period filter.
    # FALLBACK 1: re-run extraction on the rewritten query — but only if the
    #          original yielded nothing actionable (true follow-up case).
    # FALLBACK 2: regex detectors over the original question.
    slots = nlu.extract_slots(question, history=history)
    if slots is None and rewritten != question:
        slots = nlu.extract_slots(rewritten, history=history)
    nlu_metrics = []
    if slots is not None:
        all_companies = set(slots["companies"])
        all_periods = nlu.slots_to_periods(slots)
        nlu_metrics = list(slots.get("metrics") or [])
    else:
        all_companies = detect_all_companies(question)
        all_periods = detect_periods(question)
        # Range expansion: "from FY24 to FY26" -> {FY24, FY25, FY26}; "Q3 FY23
        # to Q3 FY24" -> {Q3FY23..Q3FY24}.
        all_periods = expand_period_range(all_periods, question)
        # Free-standing Q3 + annual range -> narrow each FYxx -> Q3FYxx.
        standalone_q = detect_standalone_quarter(question)
        if standalone_q and all_periods and all(p.startswith("FY") for p in all_periods):
            all_periods = {f"Q{standalone_q}{p}" for p in all_periods}
        # Last-ditch: if the original yielded nothing, retry on rewritten.
        if not all_companies and not all_periods and rewritten != question:
            all_companies = detect_all_companies(rewritten)
            all_periods = expand_period_range(detect_periods(rewritten), rewritten)
    company_filter = next(iter(all_companies)) if len(all_companies) == 1 else None
    period_filter = next(iter(all_periods)) if len(all_periods) == 1 else None
    periods_for_fanout = all_periods if len(all_periods) >= 2 else None

    # ----- Slice-2 fact cache --------------------------------------------
    # Look up cached facts BEFORE retrieval. Two outcomes:
    #   - Full coverage: the cache satisfies every requested cell, so we
    #     skip RAG and let the LLM answer from the synthetic facts chunk
    #     alone (plus a tiny per-source citation breadcrumb).
    #   - Partial / none: inject whatever cached facts we have at the top
    #     of context as authoritative supporting evidence, then run normal
    #     retrieval for the rest.
    # Cache lookup is fault-tolerant -- if anything fails we proceed with
    # the original RAG-only path.
    cached_facts = []
    cache_short_circuit = False
    cached_chunk = None
    # Uploads are not in MetricFact, so the cache can NEVER fully cover a
    # question that depends on uploaded content. has_uploads (set above)
    # is consulted below to disable the short-circuit -- we still consult
    # the cache for any curated-corpus facts that might overlap, but RAG
    # always runs.
    try:
        slots_for_cache = {
            "companies": sorted(all_companies),
            "periods":   sorted(periods_for_fanout) if periods_for_fanout
                         else ([period_filter] if period_filter else []),
            "metrics":   nlu_metrics,
            "statement": detect_statement_targets(question)[0][1]
                         if detect_statement_targets(question) else "",
        }
        cached_facts = fact_cache.lookup_for_slots(slots_for_cache)
        if cached_facts:
            cached_chunk = fact_cache.format_facts_as_context_chunk(cached_facts)
            if (config.FACT_CACHE_SHORTCIRCUIT_RAG
                    and not has_uploads
                    and fact_cache.is_full_coverage(cached_facts, slots_for_cache)):
                cache_short_circuit = True
    except Exception as e:
        print(f"  [cache] lookup failed ({type(e).__name__}: {str(e)[:80]}); RAG-only")
        cached_facts, cached_chunk, cache_short_circuit = [], None, False

    if cache_short_circuit:
        # Skip the heavy retrieval. We still pull a tiny set of source pages
        # (one per cited filename) so the LLM has the original chunk text on
        # hand to quote from and the Sources panel isn't empty.
        cited_docs = []
        seen_pages = set()
        for f in cached_facts:
            key = (f.get("source_doc"), f.get("source_page"))
            if not key[0] or key in seen_pages:
                continue
            seen_pages.add(key)
            matches = [d for d in _all_docs()
                       if d.metadata.get("source") == key[0]
                       and d.metadata.get("page") == key[1]]
            cited_docs.extend(matches[:2])  # cap per-page to keep context tight
        docs = [cached_chunk] + cited_docs[:config.MAX_CONTEXT_CHUNKS - 1]
        numeric = True  # cached facts are always numeric
    else:
        docs, numeric = retrieve(rewritten, source_filter=source_filter,
                                 multi_sources=multi_sources,
                                 company_filter=company_filter,
                                 period_filter=period_filter,
                                 periods=periods_for_fanout,
                                 top_k=mode_cfg["top_k"])
        # Per-chat uploaded PDFs: pull chunks from each upload's own Chroma
        # collection and fuse with the corpus hits. Uploads come FIRST so the
        # MAX_CONTEXT_CHUNKS cap can't drop them when the corpus also has
        # plausible hits -- the user explicitly attached this file.
        upload_docs = []
        if has_uploads:
            try:
                upload_docs = upload_store.retrieve_from_uploads(
                    rewritten, upload_ids,
                )
            except Exception as e:
                print(f"  [uploads] retrieve failed "
                      f"({type(e).__name__}: {str(e)[:120]})")
                upload_docs = []
        # Inject cached facts at the very front when we have ANY hits. They
        # take a single seat and the LLM uses them verbatim, eliminating
        # reading errors on the cells we've already validated.
        head = []
        if cached_chunk is not None:
            head.append(cached_chunk)
        # Reserve a majority of the context window for upload chunks whenever
        # uploads are attached. Without this reservation the curated corpus
        # (which is bigger + has more "project"/"revenue" hits) drowns out
        # the attached PDF and the LLM answers from corpus content the user
        # didn't ask about. The remainder of the window is filled with
        # corpus hits so cross-document questions still work.
        upload_dedup = _dedupe(upload_docs)
        if has_uploads and upload_dedup:
            reserve = max(
                len(upload_dedup),
                int(config.MAX_CONTEXT_CHUNKS * config.UPLOAD_CONTEXT_FRACTION),
            )
            upload_part = upload_dedup[:reserve]
            head.extend(upload_part)
            corpus_budget = max(0,
                config.MAX_CONTEXT_CHUNKS - len(head)
            )
            docs = head + docs[:corpus_budget]
        else:
            head.extend(upload_dedup)
            docs = head + docs
        docs = docs[:config.MAX_CONTEXT_CHUNKS]
    context = format_context(docs)

    # When the user explicitly attached a PDF, prepend a one-line steer to the
    # question so the LLM treats those chunks as the primary subject. Cheap
    # and reversible -- if the question is plainly about the corpus, the LLM
    # still has corpus chunks in context to draw from.
    asked = question
    if has_uploads and upload_docs:
        attached_names = sorted({
            d.metadata.get("source", "") for d in upload_docs
            if d.metadata.get("source")
        })
        if attached_names:
            asked = (
                f"(The user has attached the following PDF(s) to this chat: "
                f"{', '.join(attached_names)}. Treat those as the primary "
                f"document(s) the question is about, unless the question "
                f"explicitly names another company/document in the corpus.)\n\n"
                f"{question}"
            )

    chain = mode_prompt | llm | StrOutputParser()
    try:
        answer = chain.invoke({
            "context": context,
            "history": _history_to_messages(history),
            "question": asked,
        })
    except Exception as e:
        # Most common failure mode is the LLM endpoint being unreachable
        # (Ollama cloud 502, local daemon not running, model not pulled).
        # Surface a clean message instead of letting Django render a 500 page
        # the user can't act on. Retrieval already ran, so we keep `docs` for
        # the Sources panel -- the answer slot just carries the error.
        err_name = type(e).__name__
        err_text = str(e)[:300]
        print(f"  [llm] call failed: {err_name}: {err_text}")
        if "unreachable" in err_text or "502" in err_text or "Connection" in err_text:
            answer = (
                f"⚠️ The language model is unreachable right now.\n\n"
                f"Model: `{config.LLM_MODEL}`. If this name ends in "
                f"`-cloud`, the request was routed through Ollama's hosted "
                f"endpoint (ollama.com) and the connection failed. "
                f"Check your network, or switch `LLM_MODEL` in `config.py` "
                f"to a locally-pulled model (e.g. `llama3.1:8b`) and "
                f"restart the server.\n\n"
                f"Raw error: `{err_name}: {err_text}`"
            )
        else:
            answer = (
                f"⚠️ The language model call failed: "
                f"`{err_name}: {err_text}`."
            )
    # Unified slot dict for downstream consumers (fact extractor, analytics
    # layer). Mirrors what NLU produced when it succeeded, but is also
    # populated by the regex fallback path so callers don't need to care
    # which extractor ran.
    detected_statement = ""
    targets = detect_statement_targets(question)
    if targets:
        # Pick the most specific statement name detected; rarely >1 in
        # practice ('balance sheet' OR 'profit and loss' OR 'cash flow').
        detected_statement = targets[0][1]
    slots_out = {
        "companies": sorted(all_companies),
        "periods": sorted(periods_for_fanout) if periods_for_fanout
                   else ([period_filter] if period_filter else []),
        "statement": detected_statement,
        "metrics": nlu_metrics,
    }

    # Slice-3 proactive recall: find past AnalysisNotes whose scope overlaps
    # this question, so the frontend can surface "you asked this before".
    # Runs AFTER the answer so a slow DB read can't delay the response; uses
    # the same slot dict the cache uses, so scoping is consistent.
    try:
        recall_hits = analysis_recall.find_candidates(slots_out)
    except Exception as e:
        print(f"  [recall] surface failed ({type(e).__name__}: {str(e)[:120]})")
        recall_hits = []

    return {
        "answer": answer,
        "sources": docs,
        "filtered_to": source_filter,
        "multi_sources": sorted(multi_sources) if multi_sources else None,
        "numeric": numeric,
        "rewritten_query": rewritten if rewritten != question else None,
        "company_filter": company_filter,
        "period_filter": period_filter,
        "periods": sorted(periods_for_fanout) if periods_for_fanout else None,
        "companies": sorted(all_companies) if len(all_companies) >= 2 else None,
        "mode": mode_name,
        "slots": slots_out,
        # Cache diagnostics so the frontend can render "served from cache"
        # badges and we can track hit rate in logs.
        "cache_hits":           len(cached_facts),
        "cache_short_circuit":  cache_short_circuit,
        "upload_ids":           upload_ids,
        # Slice-3: prior analyses with overlapping scope. Empty list when no
        # match clears the threshold. Frontend renders these in a "Related
        # past analysis" panel above the new answer.
        "recall":               recall_hits,
    }


if __name__ == "__main__":
    if len(sys.argv) < 2:
        print("Usage: python query.py \"your question\"")
        sys.exit(1)
    q = " ".join(sys.argv[1:])
    result = ask(q)
    flags = []
    if result["filtered_to"]:
        flags.append(f"filtered to: {result['filtered_to']}")
    if result["numeric"]:
        flags.append("numeric intent → table-biased retrieval")
    if result.get("rewritten_query"):
        flags.append(f"rewritten: {result['rewritten_query']}")
    if flags:
        print("[" + " | ".join(flags) + "]")
    print("\n=== Answer ===\n")
    print(result["answer"])
    print("\n=== Sources ===")
    for i, d in enumerate(result["sources"], 1):
        kind = d.metadata.get("type", "text")
        print(f"  [{i}] {d.metadata.get('source')} p.{d.metadata.get('page')} [{kind}]")
